/*Gabriel inventou um código para representar números naturais, usando uma
sequência de zeros e uns. Funciona assim, o número natural é representado pela quantidade
de vezes que o padrão "100" aparece na sequência. Por exemplo, na sequência
11101001010011110, o padrão aparece duas vezes; e na sequência
11101010111110111010101 ele não aparece nenhuma vez. Você deve ajudar Gabriel e
implementar um programa que, dada a sequência de zeros e uns, calcule quantas vezes o
padrão "100" aparece nela.
Entrada:
A primeira linha da entrada contém um inteiro N, o tamanho da sequência. A segunda
linha contém a sequência de N zeros e uns, isto é, uma string.
Saída:
Seu programa deve imprimir um inteiro, quantas vezes o padrão "100" aparece na*/

#include <stdio.h>
int main(){

    int N = 0,val = 0;
    int i;
    scanf("%d", &N);

    char nmr[N];
    scanf("%s", nmr);

    for(i = 0; i < (N - 2); i++){
        if(nmr[i] == '1' && nmr[i+1] == '0' && nmr[i+2] == '0'){
            val++;
        }
    }

    printf("%d", val);
	return 0;
}
